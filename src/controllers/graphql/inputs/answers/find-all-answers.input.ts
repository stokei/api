import {
  OrderByDataFindAllAnswersDTO,
  WhereDataFindAllAnswersDTO
} from '@/dtos/answers/find-all-answers.dto';
import { Field, InputType } from '@nestjs/graphql';
import {
  OrderBy,
  WhereDataStringInput,
  WhereDataSearchInput,
  WherePaginated
} from '@stokei/nestjs';

@InputType()
class WhereDataFindAllAnswersDataInput implements WhereDataFindAllAnswersDTO {
  @Field(() => [String], { nullable: true })
  ids?: string[];

  @Field({ nullable: true })
  parent?: WhereDataStringInput;

  @Field({ nullable: true })
  name?: WhereDataSearchInput;
}

@InputType()
export class OrderByDataFindAllAnswersInput
  implements OrderByDataFindAllAnswersDTO
{
  @Field(() => OrderBy, { nullable: true })
  name?: OrderBy;

  @Field(() => OrderBy, { nullable: true })
  createdAt?: OrderBy;

  @Field(() => OrderBy, { nullable: true })
  updatedAt?: OrderBy;
}

@InputType()
export class WhereDataFindAllAnswersInput extends WherePaginated(
  WhereDataFindAllAnswersDataInput
) {}
